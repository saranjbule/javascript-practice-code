/**
 * Javascript is loosely/ weakly typed language
 * - a variable can hold multiple type of data types
 */

var x;
console.log(x); // undefined

x = "saranj";
console.log(x); // saranj

x = [1, 2];
console.log(x); // [1, 2]

x = 10;
console.log(x); // 10

x = undefined; // Bad practice
console.log(x) // undefined